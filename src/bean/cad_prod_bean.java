package bean;

public class cad_prod_bean {

    private String id_prod;
    private String nome_prod;
    private String descricao_prod;
    private String quantidade_prod;
    private String valor_prod;

    /**
     * @return the id_prod
     */
    public String getId_prod() {
        return id_prod;
    }

    /**
     * @param id_prod the id_prod to set
     */
    public void setId_prod(String id_prod) {
        this.id_prod = id_prod;
    }

    /**
     * @return the nome_prod
     */
    public String getNome_prod() {
        return nome_prod;
    }

    /**
     * @param nome_prod the nome_prod to set
     */
    public void setNome_prod(String nome_prod) {
        this.nome_prod = nome_prod;
    }

    /**
     * @return the descricao_prod
     */
    public String getDescricao_prod() {
        return descricao_prod;
    }

    /**
     * @param descricao_prod the descricao_prod to set
     */
    public void setDescricao_prod(String descricao_prod) {
        this.descricao_prod = descricao_prod;
    }

    /**
     * @return the quantidade_prod
     */
    public String getQuantidade_prod() {
        return quantidade_prod;
    }

    /**
     * @param quantidade_prod the quantidade_prod to set
     */
    public void setQuantidade_prod(String quantidade_prod) {
        this.quantidade_prod = quantidade_prod;
    }

    /**
     * @return the valor_prod
     */
    public String getValor_prod() {
        return valor_prod;
    }

    /**
     * @param valor_prod the valor_prod to set
     */
    public void setValor_prod(String valor_prod) {
        this.valor_prod = valor_prod;
    }
}
